using System;
using System.Collections.Generic;
using System.Text;

namespace ConsoleApp13
{
	class Point
	{
		public double x;
		public double y;
		

		public Point(double x, double y)
		{
			this.x = x;
			this.y = y;
		}
		public float GetX()
		{
			return x;
		}
		public float GetY()
		{
			return y;
		}

	}
}
